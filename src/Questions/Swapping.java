package Questions;

public class Swapping {
	public static void main(String[] args) {
		
	
		int a = 10 ;
		int b = 20 ;
		System.out.println("The Swapping of a - " +a);
		System.out.println("The Swapping of b - " +b);
		System.out.println("------------------");
		int c = a ;
		a= b;
		b= c ;
		System.out.println("The After Swapping of a - " +a);
		System.out.println("The After Swapping of b - " +b);
	}

}
